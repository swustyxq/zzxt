package swust.edu.cn.zzxt.model;

public class Reward {
    private Integer rewrId;

    private Integer rewrWorkid;

    private String rewrScholarshipid;

    private String rewrTime;

    private String rewrName;

    private String rewrAwarded;

    public Integer getRewrId() {
        return rewrId;
    }

    public void setRewrId(Integer rewrId) {
        this.rewrId = rewrId;
    }

    public Integer getRewrWorkid() {
        return rewrWorkid;
    }

    public void setRewrWorkid(Integer rewrWorkid) {
        this.rewrWorkid = rewrWorkid;
    }

    public String getRewrScholarshipid() {
        return rewrScholarshipid;
    }

    public void setRewrScholarshipid(String rewrScholarshipid) {
        this.rewrScholarshipid = rewrScholarshipid;
    }

    public String getRewrTime() {
        return rewrTime;
    }

    public void setRewrTime(String rewrTime) {
        this.rewrTime = rewrTime;
    }

    public String getRewrName() {
        return rewrName;
    }

    public void setRewrName(String rewrName) {
        this.rewrName = rewrName;
    }

    public String getRewrAwarded() {
        return rewrAwarded;
    }

    public void setRewrAwarded(String rewrAwarded) {
        this.rewrAwarded = rewrAwarded;
    }
}
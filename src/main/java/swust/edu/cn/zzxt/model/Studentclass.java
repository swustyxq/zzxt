package swust.edu.cn.zzxt.model;

public class Studentclass {
    private Integer classId;

    private Integer classMajrid;

    private String className;

    private String classShortname;

    private Integer classInstid;

    private Integer classIsspecial;

    private String classDescription;

    public Integer getClassId() {
        return classId;
    }

    public void setClassId(Integer classId) {
        this.classId = classId;
    }

    public Integer getClassMajrid() {
        return classMajrid;
    }

    public void setClassMajrid(Integer classMajrid) {
        this.classMajrid = classMajrid;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassShortname() {
        return classShortname;
    }

    public void setClassShortname(String classShortname) {
        this.classShortname = classShortname;
    }

    public Integer getClassInstid() {
        return classInstid;
    }

    public void setClassInstid(Integer classInstid) {
        this.classInstid = classInstid;
    }

    public Integer getClassIsspecial() {
        return classIsspecial;
    }

    public void setClassIsspecial(Integer classIsspecial) {
        this.classIsspecial = classIsspecial;
    }

    public String getClassDescription() {
        return classDescription;
    }

    public void setClassDescription(String classDescription) {
        this.classDescription = classDescription;
    }
}
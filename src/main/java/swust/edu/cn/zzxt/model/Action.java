package swust.edu.cn.zzxt.model;

public class Action {
    private Integer atonId;

    private String atonName;

    private String atonUrl;

    private Integer atonType;

    private String atonDescription;

    public Integer getAtonId() {
        return atonId;
    }

    public void setAtonId(Integer atonId) {
        this.atonId = atonId;
    }

    public String getAtonName() {
        return atonName;
    }

    public void setAtonName(String atonName) {
        this.atonName = atonName;
    }

    public String getAtonUrl() {
        return atonUrl;
    }

    public void setAtonUrl(String atonUrl) {
        this.atonUrl = atonUrl;
    }

    public Integer getAtonType() {
        return atonType;
    }

    public void setAtonType(Integer atonType) {
        this.atonType = atonType;
    }

    public String getAtonDescription() {
        return atonDescription;
    }

    public void setAtonDescription(String atonDescription) {
        this.atonDescription = atonDescription;
    }
}
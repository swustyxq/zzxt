package swust.edu.cn.zzxt.selfmodel;

import java.util.List;

public class AuthorityTopLevelModel {
    private int id;//id
    private int pId;//父节点Id
    private String name;//名字
    private String nocheck;//是否有checkbox
    private List<AuthorityMsg> nodes;
    private String checked;
    private boolean isParent;
    private boolean open;
    
    public boolean isParent() {
		return isParent;
	}
	public void setParent(boolean isParent) {
		this.isParent = isParent;
	}
	public boolean isOpen() {
		return open;
	}
	public void setOpen(boolean open) {
		this.open = open;
	}
	public List<AuthorityMsg> getNodes() {
        return nodes;
    }
    public void setNodes(List<AuthorityMsg> nodes) {
        this.nodes = nodes;
    }

    public String getChecked() {
        return checked;
    }
    public void setChecked(String checked) {
        this.checked = checked;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getpId() {
        return pId;
    }
    public void setpId(int pId) {
        this.pId = pId;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getNocheck() {
        return nocheck;
    }
    public void setNocheck(String nocheck) {
        this.nocheck = nocheck;
    }
    
}
